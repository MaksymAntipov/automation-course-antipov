package org.example.homeworks.hw_03;

import javax.swing.*;

public class FloatingPointDivision_08 {
    public static void main(String[] args) {
        double y = Math.abs(-8);
        double x = Math.abs(9);

        if (y >= x) {
            System.out.println("Число y больше, равняется = " + y);
        } else {
            System.out.println("Число x больше, равняется = " + x);
        }
    }
}
