package org.example.homeworks.hw_01;

public class FahrenheitToCelsius {

    public static void main(String[] args) {
        double f = 50; // ввести значение в Фаренгейтах
        double c = 5. / 9 * (f - 32);
        System.out.print("Градусов в Цельсиях: ");
        System.out.println(c);
    }
}
